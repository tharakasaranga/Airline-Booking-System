    private void jClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jClassActionPerformed
        // TODO add your handling code here:
        char classname = jClass.getSelectedItem().toString().charAt(0);
        String FlightNumber = jFlightNumber.getSelectedItem().toString();

        String query1 = "SELECT * FROM flightclass WHERE FlightNumber = '" + FlightNumber + "' AND ClassName LIKE '" + classname + "%'";
        String query2 = "SELECT * from flight where FlightNumber = '" + FlightNumber + "' ";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(url, user, databasepassword);

            Statement statement = connection.createStatement();
            ResultSet result1 = statement.executeQuery(query1);

            while(result1.next()) { // Check if there's a matching record
                tBasePrice.setText("Rs." + result1.getString(2));
                double total = Double.parseDouble(result1.getString(2)) + 35000;
                tAmount.setText("Rs." + total );
                String NumberOfSeats = result1.getString(3);
                tAvailableSeats.setText(NumberOfSeats);
            

            }

            ResultSet result2 = statement.executeQuery(query2);

            while(result2.next()) { // Check if there's a matching record
                tArrivalTime.setText(result2.getString(2));
                tDepatureTime.setText(result2.getString(3));
                
                

            }

            connection.close();

        } catch (Exception e) {
            System.out.println(e);

        }

    }//GEN-LAST:event_jClassActionPerformed

