    private void jDestinationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDestinationActionPerformed
        // TODO add your handling code here:
        jFlightNumber.removeAllItems();

        String destination = jDestination.getSelectedItem().toString();

        String query = "SELECT flightNumber FROM flight WHERE destination = '" + destination + "'";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(url, user, databasepassword);

            Statement statement = connection.createStatement();
            ResultSet result = statement.executeQuery(query);

            while (result.next()) {
                String flightNumber = result.getString("flightNumber");
                jFlightNumber.addItem(flightNumber);
            }
            connection.close();

        } catch (Exception e) {
            System.out.println(e);

        }

    }//GEN-LAST:event_jDestinationActionPerformed

